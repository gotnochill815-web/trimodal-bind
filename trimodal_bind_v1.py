def collate_fn(batch):
    images, audios, texts = zip(*batch)

    # images
    images = torch.stack(images)

    # pad audio width dimension
    max_w = max(a.shape[-1] for a in audios)

    padded = []
    for a in audios:
        pad_w = max_w - a.shape[-1]
        a = torch.nn.functional.pad(a, (0, pad_w))
        padded.append(a)

    audios = torch.stack(padded)

    # tokenize text
    tok = tokenizer(
        list(texts),
        padding=True,
        truncation=True,
        return_tensors="pt"
    )

    return images, audios, tok

