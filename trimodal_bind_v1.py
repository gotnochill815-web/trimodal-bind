def retrieve(query):
    tok = tokenizer(
        [query],
        return_tensors="pt",
        padding=True,
        truncation=True
    )

    tok = {
        "input_ids": tok["input_ids"].to(device),
        "attention_mask": tok["attention_mask"].to(device)
    }

    with torch.no_grad():
        q = txt_encoder(**tok)[0].cpu()

    best_score = -999
    best_item = None

    for item in gallery:
        score = torch.dot(q, item["emb"]).item()

        if score > best_score:
            best_score = score
            best_item = item

    return best_item["path"], f"Prediction: {best_item['label']} | Score: {best_score:.3f}"

